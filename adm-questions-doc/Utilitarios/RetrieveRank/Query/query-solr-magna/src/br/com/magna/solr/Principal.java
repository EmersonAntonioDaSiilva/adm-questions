    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(!("".equals(jTextField2.getText().trim()) &&
                "".equals(jTextField1.getText().trim()))){
            jTextField3.setText(serviceRetrieveRank.getClusterSolr(jTextField2.getText(), jTextField1.getText()));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            CollectionAdminResponse resp = serviceRetrieveRank.
                        getCollections(jTextField2.getText(), jTextField1.getText(),jTextField3.getText());
            List<String> collection = (ArrayList)resp.getResponse().get("collections");
            
            if(collection.size() > 0){
                jTextField4.setText(collection.get(0));
            }
            else{
                JOptionPane.showMessageDialog(this,"Coleção não foi encontrada.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int linha = jTable1.getSelectedRow();
        if(linha >= 0){
            String content = String.valueOf(jTable1.getValueAt(linha, 2));
            jTextArea1.setText(content);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked
    
    private void limpaTabela(DefaultTableModel tableModel){
        for(int i = tableModel.getRowCount(); i > 0; i--)
            tableModel.removeRow(i-1);
    }
    
    private boolean validaCampos(boolean validaPesquisa){
        if("".equals(jTextField2.getText().trim())){
            JOptionPane.showMessageDialog(this, "Login não informado.");
            return false;
        }
        if("".equals(jTextField1.getText().trim())){
            JOptionPane.showMessageDialog(this, "Senha não informado.");
            return false;
        }
        if("".equals(jTextField3.getText().trim())){
            JOptionPane.showMessageDialog(this, "Cluster ID não informado.");
            return false;
        }
        if("".equals(jTextField4.getText().trim())){
            JOptionPane.showMessageDialog(this, "Collection não informado.");
            return false;
        }
        
        if(validaPesquisa){
            if("".equals(jTextField5.getText().trim())){
                JOptionPane.showMessageDialog(this, "Pesquisa não informada.");
                return false;
            }
        }
        
        return true;
    }
    
    
    
    
