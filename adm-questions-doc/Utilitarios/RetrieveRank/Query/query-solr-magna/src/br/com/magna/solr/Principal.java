    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try{
            if(validaCampos(true)){

                QueryResponse res = serviceRetrieveRank.searchAllDocs(jTextField2.getText(), jTextField1.getText(), 
                                                    jTextField3.getText(), jTextField4.getText(), jTextField5.getText());
                SolrDocumentList results = res.getResults();
                
                DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
                limpaTabela(tableModel); // limpa tabela
                for (SolrDocument solrDocument : results) {
                    String[] data = new String[3];
                    Map<String,Object> valor = solrDocument.getFieldValueMap();
                    
                    // Adiciona os valores as colunas
                    data[0] = valor.get("id").toString();
                    data[1] = valor.get("title").toString();
                    data[2] = valor.get("body").toString();
                    
                    tableModel.addRow(data);
                }
                
                jTable1.setModel(tableModel);
                
                jTextArea1.setText("");
            } 
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

